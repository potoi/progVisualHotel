    private void atualizaTabela() {

        try {
            String[][] a = ficharioAtual.listar();
            String b[] = ficharioAtual.getColumnName();
            dtm.setRowCount(0);
            dtm.setColumnCount(0);

            for (String n : b) {
                dtm.addColumn(n);
            }
            for (String[] n2 : a) {
                dtm.insertRow(0, n2);
            }
        } catch (Exception ex) {
            Logger.getLogger(CrudPet.class.getName()).log(Level.SEVERE, null, ex);
        }

    }


