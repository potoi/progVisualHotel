    private void formatarCampo() {
        try {
            mask.setMask("**/**/****");
            mask.setPlaceholder("__/__/____");
            mask.install(jFTValidade);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao formatar", "ERRO", JOptionPane.ERROR);
        }
    }

    private void preencher(Produto produto) {
        jFTId.setText(Integer.toString(produto.getIdentificador()));
        jFTDescricao.setText(produto.getDescricao());
        jFTPreco.setText(Double.toString(produto.getPreco()));
        jFTMarca.setText(produto.getMarca());
        int dia, mes, ano;
        dia = produto.getValidade().getDayOfMonth();
        mes = produto.getValidade().getMonthValue();
        ano = produto.getValidade().getYear();
        String diaFormat = String.format("%02d", dia);
        String mesFormat = String.format("%02d", mes);
        jFTValidade.setText(diaFormat + "/" + mesFormat + "/" + ano);
    }

