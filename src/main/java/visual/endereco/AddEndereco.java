    private void grava() {
        try {
            Endereco endereco = new Endereco();
            if (!jFTId.getText().isEmpty()) {
                endereco.setId(Integer.parseInt(jFTId.getText()));
            }
            endereco.setBairro(jFTBairro.getText());
            endereco.setCep(jFTCep.getText());
            endereco.setComplemento(jFTComplemento.getText());
            endereco.setLogradouro(jFTLogradouro.getText());
            endereco.setNumero(jFTNumero.getText());
            endereco.setMunicipio(this.endereco.getMunicipio());
            check=1;
            this.endereco = endereco;
            this.dispose();

        } catch (Exception e) {

        }
    }
